function WordCloud({ text }: { text: string }) {
  useLayoutEffect(() => {
    const root = am5.Root.new("chartdiv");

    root.setThemes([am5themes_Animated.new(root)]);

    root.container.children.push(
      am5wc.WordCloud.new(root, {
        excludeWords: stopWords,
        text,
      }),
    );

    return () => {
      root.dispose();
    };
  }, [text]);

  return <div id="chartdiv" className="w-full h-full"></div>;
}

export default function WordCloudContainer() {
  const [userPrompts, setUserPrompts] = useState<string>();

  const retriveSession = useCallback(async () => {
    const response = await fetch("api/user-prompts", {
      method: "POST",
    });
    const json = await response.json();
    setUserPrompts(json.data);
  }, []);

  useEffect(() => {
    retriveSession();
  }, [retriveSession]);

  if (!userPrompts) return <></>;

  return <WordCloud text={userPrompts} />;
}
